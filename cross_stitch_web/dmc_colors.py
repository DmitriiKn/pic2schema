def find_closest_dmc_color(rgb):
    """
    Находит ближайший цвет DMC по RGB значениям
    Использует Euclidean distance в RGB пространстве
    """
    min_distance = float('inf')
    closest_color = None
    closest_number = None
    
    for number, color_info in DMC_COLORS.items():
        dmc_rgb = color_info["rgb"]
        # Вычисляем Euclidean distance
        distance = sum((a - b) ** 2 for a, b in zip(rgb, dmc_rgb)) ** 0.5
        
        if distance < min_distance:
            min_distance = distance
            closest_color = color_info
            closest_number = number
    
    return closest_number, closest_color["name"], closest_color["rgb"]